USE BurrowConnect;

CREATE TABLE Owner
(
    ownerID    INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(255) NOT NULL,
    last_name  VARCHAR(255) NOT NULL,
    gender     VARCHAR(50),
    email      VARCHAR(255) NOT NULL,
    bio        text,
    age        INT
);

CREATE TABLE Traveler
(
    travelerID INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(255) NOT NULL,
    last_name  VARCHAR(255) NOT NULL,
    gender     VARCHAR(50)  NOT NULL,
    age        INT          NOT NULL,
    email      VARCHAR(255) NOT NULL,
    bio        text
);

CREATE TABLE Property
(
    propertyID  INT PRIMARY KEY AUTO_INCREMENT,
    `address`     VARCHAR(255) NOT NULL,
    city        VARCHAR(100) NOT NULL,
    region      VARCHAR(50)  NOT NULL,
    country     VARCHAR(20)  NOT NULL,
    `type`        varchar(20)  NOT NULL,
    `description` text         NOT NULL,
    title       text         NOT NULL,
    price       int          NOT NULL,
    ownerID     int,


    FOREIGN KEY (ownerID) REFERENCES Owner (ownerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT


);

CREATE TABLE PropertyReview
(
    reviewID   INT PRIMARY KEY AUTO_INCREMENT,
    reviewDate DATETIME DEFAULT CURRENT_TIMESTAMP,
    content    VARCHAR(1000) NOT NULL,
    travelerID INT           NOT NULL,
    propertyID INT           NOT NULL,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (propertyID) REFERENCES Property (propertyID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE Stay_Messages
(
    messID     INT PRIMARY KEY AUTO_INCREMENT,
    content    TEXT NOT NULL,
    dateSent   DATETIME DEFAULT CURRENT_TIMESTAMP,
    travelerID INT,
    ownerID    INT  NOT NULL,
    FOREIGN KEY (ownerID) REFERENCES Owner (ownerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE Stay_At
(
    stayID     INT PRIMARY KEY AUTO_INCREMENT,
    startDate  DATE NOT NULL,
    endDate    DATE NOT NULL,
    travelerID INT,
    propertyID INT,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (propertyID) REFERENCES Property (propertyID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE ExperienceProviders
(
    providerID  INT PRIMARY KEY AUTO_INCREMENT,
    companyName VARCHAR(500),
    address     VARCHAR(250)  NOT NULL,
    city        VARCHAR(50)   NOT NULL,
    region      VARCHAR(50)   NOT NULL,
    country     VARCHAR(50)   NOT NULL,
    email       VARCHAR(50)   NOT NULL,
    description VARCHAR(2000) NOT NULL
);

CREATE TABLE Experience_Reviews
(
    reviewID   INT PRIMARY KEY AUTO_INCREMENT,
    content    VARCHAR(1000) NOT NULL,
    reviewDate DATETIME DEFAULT CURRENT_TIMESTAMP,
    providerID INT,
    travelerID INT,
    FOREIGN KEY (providerID) REFERENCES ExperienceProviders (providerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE AdLiaison
(
    liaisonID  INT PRIMARY KEY AUTO_INCREMENT,
    first_name VARCHAR(255) NOT NULL,
    last_name  VARCHAR(255) NOT NULL,
    email      VARCHAR(255) NOT NULL
);

CREATE TABLE Advertiser
(
    advertiserID INT PRIMARY KEY AUTO_INCREMENT,
    first_name   VARCHAR(255) NOT NULL,
    last_name    VARCHAR(255) NOT NULL,
    companyName  VARCHAR(500) NOT NULL,
    email        VARCHAR(255) NOT NULL,
    package      VARCHAR(50)  NOT NULL,
    liaisonID    INT,
    FOREIGN KEY (liaisonID) REFERENCES AdLiaison (liaisonID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE Experience_Ads
(
    adID         INT PRIMARY KEY AUTO_INCREMENT,
    package      VARCHAR(50) NOT NULL,
    providerID   INT,
    advertiserID INT,
    FOREIGN KEY (providerID) REFERENCES ExperienceProviders (providerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (advertiserID) REFERENCES Advertiser (advertiserID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE Travel_Data
(
    travelerID   INT,
    advertiserID INT,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (advertiserID) REFERENCES Advertiser (advertiserID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    PRIMARY KEY (travelerID, advertiserID)
);

CREATE TABLE Property_Data
(
    advertiserID INT,
    propertyID   INT,
    FOREIGN KEY (advertiserID) REFERENCES Advertiser (advertiserID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (propertyID) REFERENCES Property (propertyID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    PRIMARY KEY (advertiserID, propertyID)
);

CREATE TABLE Bundle
(
    bundleID    INT PRIMARY KEY AUTO_INCREMENT,
    description VARCHAR(2000) NOT NULL,
    type        VARCHAR(100)  NOT NULL,
    title       VARCHAR(100)  NOT NULL,
    price       INT
);

CREATE TABLE Experience
(
    experienceID INT PRIMARY KEY AUTO_INCREMENT,
    date         DATE NOT NULL,
    bundleID     INT  NOT NULL,
    travelerID   INT,
    FOREIGN KEY (bundleID) REFERENCES Bundle (bundleID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (travelerID) REFERENCES Traveler (travelerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT
);

CREATE TABLE Offer
(
    bundleID   INT,
    providerID INT,
    FOREIGN KEY (bundleID) REFERENCES Bundle (bundleID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    FOREIGN KEY (providerID) REFERENCES ExperienceProviders (providerID)
        ON UPDATE CASCADE
        ON DELETE RESTRICT,
    PRIMARY KEY (bundleID, providerID)
);

